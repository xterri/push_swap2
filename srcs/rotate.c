#include "push_swap.h"

void	rotate(int *arr, int size)
{
	int		i;
	int		tmp;

	i = -1;
	tmp = arr[0];
	while (++i < size)
		ft_swap(&arr[i], &arr[i + 1]);
	arr[i] = tmp;
}
