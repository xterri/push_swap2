#include "push_swap.h"

int		check_val(char *str)
{
	int		i;
	i = str[0] == '-' || str[0] == '+' ? 0 : -1;
	while (str[++i])
		if (!(ft_isdigit(str[i])))
			return (0);
	return (1);
}
