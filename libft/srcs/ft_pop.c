#include "libft.h"

void	ft_pop(int *arr, int *tmp, int *index)
{
	int		i;

	i = -1;
	*tmp = arr[0];
	*index -= 1;
	while (++i <= *index)
		ft_swap(&arr[i], &arr[i + 1]);
	arr[i] = 0;
}
